public class Album {
    private String ranking;
    private String title;
    private String artist;
    private String year;
    private String sales;

    public Album(String ranking, String title, String artist, String year, String sales) {
        this.ranking = ranking;
        this.title = title;
        this.artist = artist;
        this.year = year;
        this.sales = sales;
    }

}